    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        if (txtCari.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Parameter pencarian Data Obat harus diisi !");
        } else {
            absTableMasterObat = new AbsTableMasterObat(masterObatDaoImpl);
            absTableMasterObat.setListMasterObat(masterObatDaoImpl.getListData(txtCari.getText()));
            jTable1.setModel(absTableMasterObat);
            this.settingTable();
            jTable1.invalidate();
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (txtSignaObat.getText().equals("") || txtSignaHari.getText().equals("")
                || txtQty.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Data Penting Resep Dokter harus diisi !");
        } else if (!UtilityClass.isInteger(txtSignaObat.getText())
                || !UtilityClass.isInteger(txtSignaHari.getText())
                || !UtilityClass.isInteger(txtQty.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Data Resep Dokter harus dalam bentuk angka !");
        } else if (__obat == null) {
            JOptionPane.showMessageDialog(rootPane, "Salah satu Data Obat harus dipilih !");
        } else {
            int result = JOptionPane.showConfirmDialog(rootPane, "Apakah anda yakin untuk memilih Data Obat ini? ", "Pilih Data Obat", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                Date tglHabis = null;
                if (dcTgl.getDate() == null) {
                    int qty = Integer.parseInt(txtQty.getText());
                    int signaObat = Integer.parseInt(txtSignaObat.getText());
                    int totalHari = qty / signaObat;
                    
                    Calendar cal = Calendar.getInstance();
                    cal.setTime(new Date());
                    cal.set(Calendar.DAY_OF_MONTH, cal.get(Calendar.DAY_OF_MONTH) + totalHari);
                    tglHabis = cal.getTime();
                } else {
                    tglHabis = dcTgl.getDate();
                }
                __resep = new ModelResepDetail();
                __resep.setKeterangan(txtKetResep.getText());
                __resep.setObat(__obat);
                __resep.setSignaHari(Integer.parseInt(txtSignaHari.getText()));
                __resep.setSignaObat(Integer.parseInt(txtSignaObat.getText()));
                __resep.setQty(Integer.parseInt(txtQty.getText()));
                __resep.setTglHabis(tglHabis);

                this.setVisible(false);
                __form.addDataResep(__resep);
                __form.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

