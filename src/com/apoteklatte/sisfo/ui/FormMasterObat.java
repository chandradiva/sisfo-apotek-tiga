    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (__masterObat != null) {
            //update obat
            MasterObat obat = __masterObat;
            obat.setNamaObat(txtNama.getText());
            obat.setSatuanObat((SatuanObat) cbSatuan.getModel().getSelectedItem());
            obat.setStokObat(Integer.parseInt(txtStok.getText()));
            obat.setKeterangan(txtKet.getText());
            obat.setLastUpdate(new Date());
            obat.setIsActive(1);
            masterObatDaoImpl.save(obat);
            
            JOptionPane.showMessageDialog(rootPane, "Data Obat berhasil di-update !");
            this.clearForm();
            this.refreshTable();
        } else if (txtNama.getText().equals("") || cbSatuan.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Data Penting Obat harus diisi !");
        } else if (!UtilityClass.isInteger(txtStok.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Data Stok Obat harus dalam bentuk angka !");
        } else {
            //data obat baru
            MasterObat obat = new MasterObat();
            obat.setNamaObat(txtNama.getText());
            obat.setSatuanObat((SatuanObat) cbSatuan.getModel().getSelectedItem());
            obat.setStokObat(Integer.parseInt(txtStok.getText()));
            obat.setKeterangan(txtKet.getText());
            obat.setIsActive(1);
            masterObatDaoImpl.save(obat);
            
            JOptionPane.showMessageDialog(rootPane, "Data Obat baru berhasil disimpan !");
            this.clearForm();
            this.refreshTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

