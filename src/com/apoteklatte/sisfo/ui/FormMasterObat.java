    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = -1;
        index = jTable1.getSelectedRow();
        MasterObat selectedObat = absTableMasterObat.getListMasterObat().get(index);
        __masterObat = selectedObat;
        this.showData(selectedObat);
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int index = -1;
        index = jTable1.getSelectedRow();
        if (index == -1 || __masterObat == null) {
            JOptionPane.showMessageDialog(rootPane, "Pilih salah satu Data Obat yang ingin dihapus !");
        } else {
            int result = JOptionPane.showConfirmDialog(rootPane, "Apakah anda yakin menghapus data ini?", "Hapus Data", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                if (masterObatDaoImpl.delete((int) __masterObat.getId())) {
                    JOptionPane.showMessageDialog(rootPane, "Data Obat berhasil dihapus !");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Data Obat tidak berhasil dihapus !");
                }
            }
            this.clearForm();
            this.refreshTable();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (txtCari.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Nama Obat untuk dicari harus diisi !");
        } else {
            absTableMasterObat = new AbsTableMasterObat(masterObatDaoImpl);
            absTableMasterObat.setListMasterObat(masterObatDaoImpl.getListData(txtCari.getText()));
            jTable1.setModel(absTableMasterObat);
            this.settingTable();
            jTable1.invalidate();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void clearForm() {
        txtCari.setText("");
        txtKet.setText("");
        txtNama.setText("");
        txtStok.setText("0");
        cbSatuan.setSelectedIndex(-1);
        __masterObat = null;
    }
    
    private void refreshTable() {
        absTableMasterObat = new AbsTableMasterObat(masterObatDaoImpl);
        absTableMasterObat.setListMasterObat(masterObatDaoImpl.getListData());
        jTable1.setModel(absTableMasterObat);
        this.settingTable();
        jTable1.invalidate();
    }
    
    private void showData(MasterObat obat) {
        txtKet.setText(obat.getKeterangan());
        txtNama.setText(obat.getNamaObat());
        txtStok.setText(String.valueOf(obat.getStokObat()));
        cbSatuan.getModel().setSelectedItem(obat.getSatuanObat());
    }
    
