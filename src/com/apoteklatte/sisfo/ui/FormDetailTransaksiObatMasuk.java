    public void showData() {
        if (__transDistObat != null) {
            txtAlamat.setText(__transDistObat.getDistributorObat().getAlamatDistributor());
            txtNameDist.setText(__transDistObat.getDistributorObat().getNamaDistributor());
            txtTglTrans.setText(__transDistObat.getTanggalDistribusi().toString());
            txtKet.setText(__transDistObat.getKeterangan());
            
            absTableTransaksiDetailDistribusiObat = new AbsTableTransaksiDetailDistribusiObat(transaksiDetailDistribusiObatDaoImpl);
            absTableTransaksiDetailDistribusiObat.setListDetail(transaksiDetailDistribusiObatDaoImpl.getListData((int) __transDistObat.getId()));
            jTable1.setModel(absTableTransaksiDetailDistribusiObat);
            this.settingTable();
            jTable1.invalidate();
        }
    }
    
