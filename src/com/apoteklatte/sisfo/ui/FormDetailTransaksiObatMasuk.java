    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        List<TransaksiDetailDistribusiObat> listDetail = absTableTransaksiDetailDistribusiObat.getListDetail();
        List<ModelObatMasuk> listData = new ArrayList<ModelObatMasuk>();
        ModelObatMasuk model = null;
        for (TransaksiDetailDistribusiObat item : listDetail) {
            model = new ModelObatMasuk();
            model.setBatchNumber(item.getBatchNumber());
            if (item.getExpireDate() != null) {
                model.setExpireDate(item.getExpireDate());
            }
            model.setJumlahObat(item.getJumlahObat());
            model.setKeterangan(item.getKeterangan());
            model.setObat(item.getObat());
            
            listData.add(model);
        }
        this.setVisible(false);
        FormTransaksiObatMasuk form = new FormTransaksiObatMasuk(__transDistObat, __form, this, listData, 1);
        form.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

