    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int index = -1;
        index = jTable1.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(rootPane, "Pilih salah satu Data Obat yang akan di Restore !");
        } else {
            int result = JOptionPane.showConfirmDialog(rootPane, "Apakah anda yakin untuk Restore Data Obat ini ?", "Restore Data Obat", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                MasterObat selected = absTableMasterObat.getListMasterObat().get(index);
                if (masterObatDaoImpl.restore((int) selected.getId())) {
                    JOptionPane.showMessageDialog(rootPane, "Data Obat yang dipilih berhasil di Restore !");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Data Obat yang dipilih tidak berhasil di Restore");
                }
                this.refreshTable();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

