    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = -1;
        index = jTable1.getSelectedRow();
        Dokter selected = absTableDokter.getListDokter().get(index);
        __dokter = selected;
        this.showData(selected);
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtNama.getText().equals("") || txtIzin.getText().equals("")
                || txtTelp.getText().equals("") || cbGender.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Semua Data Penting Dokter harus diisi !");
        } else {
            Dokter dokter = new Dokter();
            dokter.setAlamatDokter(txtAlamat.getText());
            dokter.setGender((String) cbGender.getSelectedItem());
            dokter.setKeterangan(txtKet.getText());
            dokter.setIsActive(1);
            dokter.setNamaDokter(txtNama.getText());
            dokter.setNoIzinPraktek(txtIzin.getText());
            dokter.setNoTelp(txtTelp.getText());
            dokterDaoImpl.save(dokter);
            
            JOptionPane.showMessageDialog(rootPane, "Data Dokter baru berhasil disimpan !");
            this.refreshTable();
            this.clearForm();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (txtCari.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Parameter pencarian Data Dokter harus diisi !");
        } else {
            absTableDokter = new AbsTableDokter(dokterDaoImpl);
            absTableDokter.setListDokter(dokterDaoImpl.getListData(txtCari.getText()));
            jTable1.setModel(absTableDokter);
            this.settingTable();
            jTable1.invalidate();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (__dokter == null) {
            JOptionPane.showMessageDialog(rootPane, "Salah satu Data Dokter harus dipilih terlebih dahulu !");
        } else {
            int result = JOptionPane.showConfirmDialog(rootPane, "Apakah anda yakin memilih Data Dokter ini? ", "Select Data Dokter", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                this.setVisible(false);
                __form.setDokter(__dokter);
                __form.showDataDokter();
                __form.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void clearForm() {
        txtAlamat.setText("");
        txtCari.setText("");
        txtIzin.setText("");
        txtKet.setText("");
        txtNama.setText("");
        txtTelp.setText("");
        __dokter = null;
        
        this.refreshTable();
    }
    
    private void refreshTable() {
        absTableDokter = new AbsTableDokter(dokterDaoImpl);
        absTableDokter.setListDokter(dokterDaoImpl.getListData());
        jTable1.setModel(absTableDokter);
        this.settingTable();
        jTable1.invalidate();
    }
    
    private void settingTable() {
        jTable1.getColumnModel().getColumn(0).setMaxWidth(50);
        jTable1.getColumnModel().getColumn(1).setMaxWidth(150);
        jTable1.getColumnModel().getColumn(2).setMaxWidth(170);
        jTable1.getColumnModel().getColumn(3).setMaxWidth(90);
        jTable1.getColumnModel().getColumn(4).setMaxWidth(100);
        jTable1.getColumnModel().getColumn(5).setMaxWidth(200);
        jTable1.getColumnModel().getColumn(6).setMaxWidth(150);
    }
    
    private void showData(Dokter dokter) {
        txtAlamat.setText(dokter.getAlamatDokter());
        txtIzin.setText(dokter.getNoIzinPraktek());
        txtKet.setText(dokter.getKeterangan());
        txtNama.setText(dokter.getNamaDokter());
        txtTelp.setText(dokter.getNoTelp());
    }
    
