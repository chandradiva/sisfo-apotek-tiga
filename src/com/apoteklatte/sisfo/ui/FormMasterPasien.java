    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtAlamat.getText().equals("")
                || txtNama.getText().equals("")
                || cbGender.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Data Penting Pasien harus diisi !");
        } else if (!UtilityClass.isInteger(txtUmur.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Data Umur Pasien harus dalam bentuk Angka !");
        } else if (__pasien != null) {
            //update data
            Pasien pasien = __pasien;
            pasien.setAlamatPasien(txtAlamat.getText());
            pasien.setGender((String)cbGender.getSelectedItem());
            pasien.setIsActive(1);
            pasien.setKeterangan(txtKeterangan.getText());
            pasien.setNamaPasien(txtNama.getText());
            pasien.setNoTelp(txtTelp.getText());
            pasien.setUmurPasien(Integer.parseInt(txtUmur.getText()));
            pasienDaoImpl.save(pasien);
            
            JOptionPane.showMessageDialog(rootPane, "Data Pasien yang dipilih berhasil di-update !");
            this.clearForm();
            this.refreshTable();
        } else {
            //data baru
            Pasien pasien = new Pasien();
            pasien.setAlamatPasien(txtAlamat.getText());
            pasien.setGender((String)cbGender.getSelectedItem());
            pasien.setIsActive(1);
            pasien.setKeterangan(txtKeterangan.getText());
            pasien.setNamaPasien(txtNama.getText());
            pasien.setNoTelp(txtTelp.getText());
            pasien.setUmurPasien(Integer.parseInt(txtUmur.getText()));
            pasienDaoImpl.save(pasien);
            
            JOptionPane.showMessageDialog(rootPane, "Data Pasien baru berhasil disimpan !");
            this.clearForm();
            this.refreshTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = -1;
        index = jTable1.getSelectedRow();
        Pasien selected = absTablePasien.getListPasien().get(index);
        __pasien = selected;
        this.showData(__pasien);
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int index = -1;
        index = jTable1.getSelectedRow();
        if (__pasien == null || index == -1) {
            JOptionPane.showMessageDialog(rootPane, "Pilih salah satu Data Pasien yang akan dihapus !");
        } else {
            int result = JOptionPane.showConfirmDialog(rootPane, "Apakah anda yakin menghapus Data Pasien ini?", "Hapus Data Pasien", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                if (pasienDaoImpl.delete((int) __pasien.getId())) {
                    JOptionPane.showMessageDialog(rootPane, "Data Pasien berhasil dihapus !");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Data Pasien tidak berhasil dihapus !");
                }
            }
            this.clearForm();
            this.refreshTable();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (txtCari.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Parameter pencarian Data Pasien harus diisi !");
        } else {
            absTablePasien = new AbsTablePasien(pasienDaoImpl);
            absTablePasien.setListPasien(pasienDaoImpl.getListData(txtCari.getText()));
            jTable1.setModel(absTablePasien);
            this.settingTable();
            jTable1.invalidate();
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void settingTable() {
        jTable1.getColumnModel().getColumn(0).setMaxWidth(60);
        jTable1.getColumnModel().getColumn(1).setMaxWidth(200);
        jTable1.getColumnModel().getColumn(2).setMaxWidth(50);
        jTable1.getColumnModel().getColumn(3).setMaxWidth(110);
        jTable1.getColumnModel().getColumn(4).setMaxWidth(110);
        jTable1.getColumnModel().getColumn(5).setMaxWidth(210);
        jTable1.getColumnModel().getColumn(6).setMaxWidth(200);
        jTable1.getColumnModel().getColumn(7).setMaxWidth(180);
    }
    
    private void clearForm() {
        txtAlamat.setText("");
        txtCari.setText("");
        txtKeterangan.setText("");
        txtNama.setText("");
        txtTelp.setText("");
        txtUmur.setText("");
        cbGender.setSelectedIndex(-1);
        __pasien = null;
    }
    
    private void refreshTable() {
        absTablePasien = new AbsTablePasien(pasienDaoImpl);
        absTablePasien.setListPasien(pasienDaoImpl.getListData());
        jTable1.setModel(absTablePasien);
        this.settingTable();
        jTable1.invalidate();
    }
    
    private void showData(Pasien pasien) {
        txtAlamat.setText(pasien.getAlamatPasien());
        txtKeterangan.setText(pasien.getKeterangan());
        txtNama.setText(pasien.getNamaPasien());
        txtTelp.setText(pasien.getNoTelp());
        txtUmur.setText(String.valueOf(pasien.getUmurPasien()));
        cbGender.setSelectedItem(pasien.getGender());
    }
    
